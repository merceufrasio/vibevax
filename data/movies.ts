import type { CastMember, Episode, Movie } from "@/types/movie";

type MovieSeed = Omit<Movie, "poster" | "backdrop" | "cast" | "episodes"> & {
  castNames: string[];
};

const posterFor = (seed: string) =>
  `https://picsum.photos/seed/${seed}-poster/600/900`;

const backdropFor = (seed: string) =>
  `https://picsum.photos/seed/${seed}-backdrop/1280/720`;

const avatarFor = (seed: string, index: number) =>
  `https://picsum.photos/seed/${seed}-cast-${index + 1}/160/160`;

const buildCast = (seed: string, names: string[]): CastMember[] =>
  names.map((name, index) => ({
    id: `${seed}-cast-${index + 1}`,
    name,
    role:
      index === 0
        ? "Vai chính"
        : index === 1
          ? "Người đồng hành"
          : "Nhân vật nổi bật",
    avatar: avatarFor(seed, index),
  }));

const buildEpisodes = (count: number): Episode[] =>
  Array.from({ length: count }, (_, index) => ({
    id: `episode-${index + 1}`,
    number: index + 1,
    title: `Tập ${index + 1}`,
    durationMinutes: 42 + (index % 3) * 4,
    isNew: index + 1 === count,
  }));

